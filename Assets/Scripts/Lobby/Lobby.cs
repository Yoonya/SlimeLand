using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using BackEnd;

public class Lobby : MonoBehaviour
{
    public GameObject status;
    public GameObject shop;
    public GameObject ranking;
    public GameObject admob;

    private void Awake()
    {
        //뒤끝 서버 연결
        var bro = Backend.Initialize(true);
        if (bro.IsSuccess())
        {
            // 초기화 성공 시 로직
            //Debug.Log("초기화 성공!");
        }
        else
        {
            // 초기화 실패 시 로직
            //Debug.LogError("초기화 실패!");
        }
    }
    // Start is called before the first frame update
    void Start()
    {
        AudioManager.instance.PlayBGM("BGM0");
        admob.SetActive(true);
        SetResolution();
    }

    public void StartBtn()
    {
        AudioManager.instance.PlaySFX("Button");
        admob.SetActive(false);
        StartCoroutine(LoadScene("Game"));
    }

    public void SlimeBtn()
    {
        AudioManager.instance.PlaySFX("Button");
        status.SetActive(true);
    }

    public void RankingBtn()
    {
        AudioManager.instance.PlaySFX("Button");
        ranking.SetActive(true);
    }

    public void ShopBtn()
    {
        AudioManager.instance.PlaySFX("Button");
        shop.SetActive(true);
    }


    public void OptionBtn()
    {
        AudioManager.instance.PlaySFX("Button");
    }

    public void QuitBtn()
    {
        AudioManager.instance.PlaySFX("Button");
        Application.Quit();
    }

    IEnumerator LoadScene(string nextScene)
    {
        yield return new WaitForSeconds(1);
        LoadingScene.LoadScene(nextScene);
    }

    // 해상도 설정하는 함수
    public void SetResolution()
    {
        int setWidth = 1920; // 사용자 설정 너비
        int setHeight = 1080; // 사용자 설정 높이

        int deviceWidth = Screen.width; // 기기 너비 저장
        int deviceHeight = Screen.height; // 기기 높이 저장

        Screen.SetResolution(setWidth, (int)(((float)deviceHeight / deviceWidth) * setWidth), true); // SetResolution 함수 제대로 사용하기

        if ((float)setWidth / setHeight < (float)deviceWidth / deviceHeight) // 기기의 해상도 비가 더 큰 경우
        {
            float newWidth = ((float)setWidth / setHeight) / ((float)deviceWidth / deviceHeight); // 새로운 너비
            Camera.main.rect = new Rect((1f - newWidth) / 2f, 0f, newWidth, 1f); // 새로운 Rect 적용
        }
        else // 게임의 해상도 비가 더 큰 경우
        {
            float newHeight = ((float)deviceWidth / deviceHeight) / ((float)setWidth / setHeight); // 새로운 높이
            Camera.main.rect = new Rect(0f, (1f - newHeight) / 2f, 1f, newHeight); // 새로운 Rect 적용
        }
    }
}
